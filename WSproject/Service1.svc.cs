        public string GetData(int value, int multiplicar)
        {
            int rpta;
            string msj;
            if (value > 10)
            {
                msj = "no se puede multiplicar numeros mayores a : " + value;
            }
            else
            {
                multiplicar = 0;
                value = value * multiplicar;
